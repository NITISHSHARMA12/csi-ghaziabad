		<center><u><h2>About CSI</h2></u></center><br>
		<p align="justify">
		The seed for the Computer Society of India (CSI) was first sown in the year 1965 with a handful of IT enthusiasts who were a computer user group and felt the need to organize their activities. They also wanted to share their knowledge and exchange ideas on what they felt was a fast emerging sector. Today the CSI takes pride in being the largest and most professionally managed association of and for IT professionals in India. The purposes of the Society are scientific and educational directed towards the advancement of the theory and practice of computer science and IT. The organisation has grown to an enviable size of 100,000 strong members consisting of professionals with varied backgrounds including Software developers, Scientists, Academicians, Project Managers, CIO's , CTO's & IT vendors to just name a few. It has spread its branches all over the country. Currently having more than 500 student branches and rooted firmly at 73 different locations, CSI has plans of opening many more chapters & activity centers in smaller towns and cities of the country. The idea is to spread the knowledge, and provide opportunities to as many interested as possible.</p>
		
		<u><h3><center>The CSI Vision: "lT for Masses"</u></h3></center><br>
		<p align="justify">
		Keeping in mind the interest of the IT professionals & computer users CSI works towards making the profession an area of choice amongst all sections of the society. The promotion of Information Technology as a profession is the top priority of CSI today. To fulfill this objective, the CSI regularly organizes conferences, conventions, lectures, projects, awards. And at the same time it also ensures that regular training and skill updating are organised for the IT professionals. Education Directorate, CSI helps physically challenged citizens by providing training 'Punarjani'. CSI also works towards a global approach, by seeking out alliances with organizations overseas who may be willing to come forward and participate in such activities. CSI also helps governments in formulating IT strategy & planning.
		</p>
